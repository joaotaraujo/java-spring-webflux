public class MusicsData {

    public static void main(String[] args) {
        AmazonDynamoDB client = AmazonDynamoDBClientBuilder.standard()
                .withEndpointConfiguration(new AwsClientBuilder.EndpointConfiguration(ENDPOINT_DYNAMO, REGION_DYNAMO))
                .build();

        DynamoDB dynamoDB = new DynamoDB(client);
        Table table = dynamoDB.getTable("Musics_Table");

        Item music = new Item()
                .withPrimaryKey("id","1")
                .withString("name", "Caancao da America")
                .withString("autor", "Milton Nascimento")
                .withNumber("creationYear",1967);

        PutItemOutcome outcome = table.putItem(music);
    }

}
